    private void bxBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bxBayarActionPerformed
        int total, bayar, kembalian;

        total = Integer.valueOf(bxTotalBayar.getText());
        bayar = Integer.valueOf(bxBayar.getText());

        if (total > bayar) {
            JOptionPane.showMessageDialog(null, "Maaf, Uang Anda Tidak Cukup");
        } else {
            kembalian = bayar - total;
            bxKembalian.setText(String.valueOf(kembalian));
        }
    }//GEN-LAST:event_bxBayarActionPerformed

