    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        DefaultTableModel model = (DefaultTableModel) ItemList.getModel();

        String noTransaksi = bxNoTransaksi.getText();
        String tanggal = bxTanggal.getText();
        String idCustomer = bxIDCustomer.getText();
        String total = bxTotalBayar.getText();

        try {
            Connection cnn = koneksi.getConnection();
            String sql = "INSERT INTO penjualan VALUES (?, ?, ?, ?)";
            PreparedStatement p = cnn.prepareStatement(sql);
            p.setString(1, noTransaksi);
            p.setString(2, tanggal);
            p.setString(3, idCustomer);
            p.setString(4, total);
            p.executeUpdate();
            p.close();
        } catch (Exception e) {
            System.out.println("Maaf, Terjadi Kesalahan Terhadap Sistem");
        }

        try {
            Connection cnn = koneksi.getConnection();
            int baris = ItemList.getRowCount();
            for(int i = 0; i < 10; i++) {
                String sql = "INSERT INTO penjualanrinci(NoFaktur, Id_barang, Nama_barang, Jumlah, Harga, Total) VALUES('"
                + ItemList.getValueAt(i, 0) + "','"
                + ItemList.getValueAt(i, 1) + "','"
                + ItemList.getValueAt(i, 2) + "','"
                + ItemList.getValueAt(i, 3) + "','"
                + ItemList.getValueAt(i, 4) + "','"
                + ItemList.getValueAt(i, 5) + "')";
                PreparedStatement p = cnn.prepareStatement(sql);
                p.executeUpdate();
                p.close();
            }
        } catch (Exception e) {
            System.out.println("Terjadi Error Terhadap Penyimpanan Rinci");
        }
        clear();
        utama();
        autonumber();
        kosong();
        bxTampil.setText("Rp. 0");
    }//GEN-LAST:event_btnSimpanActionPerformed

